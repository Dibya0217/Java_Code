package com.Operators;

public class Shift_Operators {
	public static void main(String[] args) {
		int a = 4;
		System.out.println(a >> 1);
		System.out.println(a >>> 1);
		System.out.println(a << 1);
		int b = -4;
		System.out.println(b >> 1);
		System.out.println(b >>> 1);
		System.out.println(b << 1);
	}
}
